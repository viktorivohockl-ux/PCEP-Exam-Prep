def classify_number(value):
  try:
    num = float(value)
    if num == int(num):
      # It's an integer (e.g., 5, 5.0)
      int_num = int(num)
      if int_num % 2 == 0:
        return "even"
      else:
        return "odd"
    else:
      # It's a float with a non-zero decimal part
      return "decimal"
  except (ValueError, TypeError):
    # Cannot convert to a number
    return "not a number"

